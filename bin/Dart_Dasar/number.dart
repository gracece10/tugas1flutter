void main() {
  //Tipe Data Number
  int number1 = 10; // int untuk bilangan bulat
  double number2 = 10.5; //double untuk bilangan desimal

  print(number1);
  print(number2);

  //Tipe Data Num bisa bilangan bulat dan double atau yg bisa int dan double
  num number = 10;
  print(number);

  number = 10.5;
  print(number);
}
